#드림핵 리버싱 basic_8번 문제
#해석 코드 파일


buf = [0xAC, 0xF3, 0x0C, 0x25, 0xA3, 0x10, 0xB7, 0x25, 0x16, 0xC6, 0xB7, 0xBC, 0x07, 0x25, 0x02, 0xD5, 0xC6, 0x11, 0x07, 0xC5, 0x00]

result = ""

for byte in buf:
    for i in range(256):
        a = byte + i * 256
        if a % 0xfb == 0:
            result += chr(a // 0xfb)

print(result)
